class Api::PagesController < ApplicationController

  # ==============================================
  # ACTIONS ======================================
  # ==============================================

  # ----------------------------------------------
  # SHOW -----------------------------------------
  # ----------------------------------------------
  def show
    @page = @brand.brand_pages.where(published: true, slug: params[:id]).first
    render json: @page
  end

end