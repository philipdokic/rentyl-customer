# ================================================
# RUBY->MODEL->BOOKING ===========================
# ================================================
class Booking < ApplicationRecord

  # ----------------------------------------------
  # DATABASE -------------------------------------
  # ----------------------------------------------
  connects_to database: { writing: :direct, reading: :direct_replica }

  # ----------------------------------------------
  # RELATIONS ------------------------------------
  # ----------------------------------------------
  belongs_to :unit_listing

  has_one :unit, through: :unit_listing

end